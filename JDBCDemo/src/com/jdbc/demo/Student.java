package com.jdbc.demo;

public class Student {
	private Integer age;
	private String name;
	private Integer id;
	
	public void setAge(Integer age){
		this.age = age;
	}
	public Integer getAge() {
		return age;
	}
	public void setName(String Name){
		this.name = Name;
	}
	public String getName() {
		return name;
	}
	public void setID(Integer id) {
		this.id = id;
	}
	public Integer getID() {
		return id;
	}
}
